package org.youngjin.net.inbound;

public class OnhandSum {
	private int sumGross;
	private int sumNet;
	private int sumCuft;
	private int piece;
	public int getPiece() {
		return piece;
	}
	public void setPiece(int piece) {
		this.piece = piece;
	}
	public int getSumGross() {
		return sumGross;
	}
	public void setSumGross(int sumGross) {
		this.sumGross = sumGross;
	}
	public int getSumNet() {
		return sumNet;
	}
	public void setSumNet(int sumNet) {
		this.sumNet = sumNet;
	}
	public int getSumCuft() {
		return sumCuft;
	}
	public void setSumCuft(int sumCuft) {
		this.sumCuft = sumCuft;
	}
	
}
